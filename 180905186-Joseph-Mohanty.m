On the 2nd of October 2014, the Swacch Bharat Abhiyan (Mission) was launched which aimed for a cleaner India.
According to the Press Information Bureau **62 billion tonnes of waste** is generated in India with annual growth of 4%.
This affects the GDP of the country by *6.4%*.
Along with garbage __OPEN DEFECATION__ and __SEWAGE CONTAMINATION__ has been an endemic sanitation problem in India.
In 2014, India was the country with the highest number of people practicing open defecation, around *530 million*.

After the mission was launched,the government constructed 86 million toilets, reducing the number of open defecators from __550 million to 1 million__.
This national campaign spanned 4041 stuatory cities and towns across India.
